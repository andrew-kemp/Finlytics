        "Title","CompanyEntryType","Amount","EffectiveDate","PeriodKey","TaxYear","FinancialYear","Notes"
    )

    # =======================
    # Company Documents Library
    # =======================
    Ensure-List -Title "Company Documents" -Template "DocumentLibrary" -Url "CompanyDocuments" -OnQuickLaunch
    
    # Comprehensive document type choices covering all use cases
    Ensure-Field -List "Company Documents" -DisplayName "Document Type" -InternalName "DocumentType" -Type Choice -Choices @(
        "Company Logo",
        "Director Signature",
        "Officer Signature",
        "Share Certificate",
        "Template",
        "Meeting Minutes",
        "Articles of Association",
        "Financial Statement",
        "Contract",
        "Policy",
        "Tax Return",
        "Correspondence",
        "Legal Document",
        "Other"
    )
    
    # Metadata fields for flexible document management
    Ensure-Field -List "Company Documents" -DisplayName "Person Name"    -InternalName "PersonName"    -Type Text
    Ensure-Field -List "Company Documents" -DisplayName "Person Title"   -InternalName "PersonTitle"   -Type Text
    Ensure-Field -List "Company Documents" -DisplayName "Is Active"      -InternalName "IsActive"      -Type Boolean
    Ensure-Field -List "Company Documents" -DisplayName "Related Entity" -InternalName "RelatedEntity" -Type Text
    Ensure-Field -List "Company Documents" -DisplayName "Document Date"  -InternalName "DocumentDate"  -Type DateTime
    Ensure-Field -List "Company Documents" -DisplayName "Expiry Date"    -InternalName "ExpiryDate"    -Type DateTime
    Ensure-Field -List "Company Documents" -DisplayName "Notes"          -InternalName "DocNotes"      -Type Note

    # Default view showing all key fields
    Ensure-View -List "Company Documents" -Title "All Company Documents" -Fields @(
        "FileLeafRef","DocumentType","PersonName","PersonTitle","IsActive","RelatedEntity","DocumentDate","DocNotes","Modified"
    )

    # =======================
    # Company Settings (seeded)
    # =======================
    Ensure-List -Title "Company Settings" -Template "GenericList" -EnableAttachments:$false -Url "CompanySettings" -OnQuickLaunch

    # Identity & contact
    Ensure-Field -List "Company Settings" -DisplayName "Profile Name"       -InternalName "Title"                 -Type Text -Indexed:$true -EnforceUnique:$true
    Ensure-Field -List "Company Settings" -DisplayName "Business Name"      -InternalName "BusinessName"          -Type Text
    Ensure-Field -List "Company Settings" -DisplayName "Trading Name"       -InternalName "TradingName"           -Type Text
    Ensure-Field -List "Company Settings" -DisplayName "Contact Name"       -InternalName "ContactName"           -Type Text
    Ensure-Field -List "Company Settings" -DisplayName "Invoicing Email"    -InternalName "InvoicingEmail"        -Type Text
    Ensure-Field -List "Company Settings" -DisplayName "Invoicing Phone"    -InternalName "InvoicingPhone"        -Type Text
    Ensure-Field -List "Company Settings" -DisplayName "Registered Address" -InternalName "RegisteredAddress"     -Type Note
    Ensure-Field -List "Company Settings" -DisplayName "Invoices Mailbox"   -InternalName "InvoicesEmail"         -Type Text
    Ensure-Field -List "Company Settings" -DisplayName "Quotes Mailbox"     -InternalName "QuotesEmail"           -Type Text
    Ensure-Field -List "Company Settings" -DisplayName "Payments Mailbox"   -InternalName "PaymentsEmail"         -Type Text

    # Registration & tax
    Ensure-Field -List "Company Settings" -DisplayName "Company Reg No"     -InternalName "CompanyRegNo"          -Type Text
    Ensure-Field -List "Company Settings" -DisplayName "VAT Reg No"         -InternalName "VATRegNo"              -Type Text
    Ensure-Field -List "Company Settings" -DisplayName "Default VAT Rate"   -InternalName "DefaultVATRate"        -Type Number
    Ensure-Field -List "Company Settings" -DisplayName "Base Currency"      -InternalName "BaseCurrency"          -Type Text
    Ensure-Field -List "Company Settings" -DisplayName "Currency Symbol"    -InternalName "CurrencySymbol"        -Type Text

    # FY boundary
    Ensure-Field -List "Company Settings" -DisplayName "Company Inception Date" -InternalName "CompanyInceptionDate" -Type DateTime
    Ensure-Field -List "Company Settings" -DisplayName "FY Start Month"     -InternalName "FYStartMonth"          -Type Number
    Ensure-Field -List "Company Settings" -DisplayName "FY Start Day"       -InternalName "FYStartDay"            -Type Number

    # Invoice numbering & payment terms
    Ensure-Field -List "Company Settings" -DisplayName "Invoice Prefix"     -InternalName "InvoicePrefix"         -Type Text
    Ensure-Field -List "Company Settings" -DisplayName "Quote Prefix"       -InternalName "QuotePrefix"           -Type Text
    Ensure-Field -List "Company Settings" -DisplayName "Next Invoice No"    -InternalName "NextInvoiceNo"         -Type Number
    Ensure-Field -List "Company Settings" -DisplayName "Payment Terms (days)" -InternalName "PaymentTermsDays"    -Type Number
    Ensure-Field -List "Company Settings" -DisplayName "Invoice Footer Text" -InternalName "InvoiceFooterText"    -Type Note
    Ensure-Field -List "Company Settings" -DisplayName "Company Logo URL"   -InternalName "LogoUrl"               -Type URL

    # Bank details (optional)
    Ensure-Field -List "Company Settings" -DisplayName "Bank Account Name"  -InternalName "BankAccountName"       -Type Text
    Ensure-Field -List "Company Settings" -DisplayName "Sort Code"          -InternalName "BankSortCode"          -Type Text
    Ensure-Field -List "Company Settings" -DisplayName "Account Number"     -InternalName "BankAccountNumber"     -Type Text
    Ensure-Field -List "Company Settings" -DisplayName "IBAN"               -InternalName "BankIBAN"              -Type Text
    Ensure-Field -List "Company Settings" -DisplayName "SWIFT/BIC"          -InternalName "BankSWIFT"             -Type Text

    # SMTP Settings (for SMTP2Go email)
    Ensure-Field -List "Company Settings" -DisplayName "SMTP Server"         -InternalName "SmtpServer"            -Type Text
    Ensure-Field -List "Company Settings" -DisplayName "SMTP Port"           -InternalName "SmtpPort"              -Type Number
    Ensure-Field -List "Company Settings" -DisplayName "SMTP From Address"   -InternalName "SmtpFromAddress"       -Type Text
    Ensure-Field -List "Company Settings" -DisplayName "SMTP Username"       -InternalName "SmtpUsername"          -Type Text

    # Director information
    Ensure-Field -List "Company Settings" -DisplayName "Director Name"       -InternalName "DirectorName"          -Type Text
    Ensure-Field -List "Company Settings" -DisplayName "Director Signature"  -InternalName "DirectorSignature"     -Type Note
    Ensure-Field -List "Company Settings" -DisplayName "Has Authorized Officer" -InternalName "HasAuthorizedOfficer" -Type Boolean
    Ensure-Field -List "Company Settings" -DisplayName "Authorized Officer"  -InternalName "AuthorizedOfficerName" -Type Text
    Ensure-Field -List "Company Settings" -DisplayName "Officer Signature"   -InternalName "AuthorizedOfficerSignature" -Type Note

    # Seed a single default item if none exists
    try {
        $existingCompanyItem = Get-PnPListItem -List "Company Settings" -PageSize 1 | Select-Object -First 1
        if (-not $existingCompanyItem) {
            Add-PnPListItem -List "Company Settings" -Values @{
                Title            = "Default"
                BusinessName     = "Your Business Ltd"
                BaseCurrency     = $baseCurrencyCode
                DefaultVATRate   = 20
                FYStartMonth     = 1
                FYStartDay       = 1
                PaymentTermsDays = 14
                SmtpServer       = "mail.smtp2go.com"
                SmtpPort         = 587
            } | Out-Null
