        }
    } catch {
        Write-Warning ("Could not seed Company Settings: {0}" -f $_.Exception.Message)
    }

    # Apply currency LCID to all Currency fields (Ledger)
    Set-CurrencyOnField -List "Ledger" -FieldInternalName "AmountNet" -CurrencyLcid $currencyLcid
    Set-CurrencyOnField -List "Ledger" -FieldInternalName "VATAmount" -CurrencyLcid $currencyLcid
    Set-CurrencyOnField -List "Ledger" -FieldInternalName "AmountGross" -CurrencyLcid $currencyLcid

    # =======================
    # Azure Key Vault Setup (for SMTP password)
    # =======================
    Write-Host "`n=== Azure Key Vault Setup ===" -ForegroundColor Cyan
    
    $createKeyVault = Read-Host "Do you want to create/configure Azure Key Vault for SMTP password? (Y/N)"
    if ($createKeyVault -eq "Y" -or $createKeyVault -eq "y") {
        try {
            # Check if Azure CLI is installed
            $azInstalled = Get-Command az -ErrorAction SilentlyContinue
            if (-not $azInstalled) {
                Write-Warning "Azure CLI (az) not found. Please install it from: https://aka.ms/installazurecliwindows"
                Write-Host "Skipping Key Vault setup..." -ForegroundColor Yellow
            } else {
                # Get or prompt for Key Vault details
                $resourceGroup = Get-IniValueOrPrompt -Path $configFile -Section "Azure" -Key "ResourceGroup" -Prompt "Azure Resource Group name" -Default "rg-financehub-prod"
                $keyVaultName = Get-IniValueOrPrompt -Path $configFile -Section "Azure" -Key "KeyVaultName" -Prompt "Key Vault name (must be globally unique)" -Default "kv-financehub-$tenantShort"
                $location = Get-IniValueOrPrompt -Path $configFile -Section "Azure" -Key "Location" -Prompt "Azure location" -Default "uksouth"
                $functionAppName = Get-IniValueOrPrompt -Path $configFile -Section "Azure" -Key "FunctionAppName" -Prompt "Function App name" -Default "func-financehub-2669"

                Write-Host "Checking if logged into Azure..." -ForegroundColor Yellow
                $account = az account show 2>$null | ConvertFrom-Json
                if (-not $account) {
                    Write-Host "Logging into Azure..." -ForegroundColor Yellow
                    az login
                }

                # Check if Resource Group exists
                $rgExists = az group exists --name $resourceGroup | ConvertFrom-Json
                if (-not $rgExists) {
                    Write-Host "Creating Resource Group: $resourceGroup" -ForegroundColor Yellow
                    az group create --name $resourceGroup --location $location | Out-Null
                    Write-Host "✓ Resource Group created" -ForegroundColor Green
                } else {
                    Write-Host "✓ Resource Group exists" -ForegroundColor Green
                }

                # Check if Key Vault exists
                $kvExists = az keyvault list --resource-group $resourceGroup --query "[?name=='$keyVaultName'].name" -o tsv
                if (-not $kvExists) {
                    Write-Host "Creating Key Vault: $keyVaultName" -ForegroundColor Yellow
                    az keyvault create --name $keyVaultName --resource-group $resourceGroup --location $location --enable-rbac-authorization false | Out-Null
                    Write-Host "✓ Key Vault created" -ForegroundColor Green
                } else {
                    Write-Host "✓ Key Vault exists" -ForegroundColor Green
                }

                # Prompt for SMTP password
                Write-Host "`nSMTP2Go Configuration:" -ForegroundColor Cyan
                $smtpPassword = Read-Host "Enter SMTP2Go password (or press Enter to skip)" -AsSecureString
                $smtpPasswordPlain = [Runtime.InteropServices.Marshal]::PtrToStringAuto([Runtime.InteropServices.Marshal]::SecureStringToBSTR($smtpPassword))
                
                if (-not [string]::IsNullOrWhiteSpace($smtpPasswordPlain)) {
                    Write-Host "Setting SMTP password in Key Vault..." -ForegroundColor Yellow
                    az keyvault secret set --vault-name $keyVaultName --name "SmtpPassword" --value $smtpPasswordPlain | Out-Null
                    Write-Host "✓ SMTP password stored in Key Vault" -ForegroundColor Green
                    
                    # Clear sensitive variable
                    $smtpPasswordPlain = $null
                }

                # Grant Function App access to Key Vault
                Write-Host "Granting Function App access to Key Vault..." -ForegroundColor Yellow
                
                # Get Function App principal ID
                $principalId = az functionapp identity show --name $functionAppName --resource-group $resourceGroup --query principalId -o tsv 2>$null
                
                if ($principalId) {
                    az keyvault set-policy --name $keyVaultName --object-id $principalId --secret-permissions get list | Out-Null
                    Write-Host "✓ Function App has access to Key Vault secrets" -ForegroundColor Green
                } else {
                    Write-Warning "Function App not found or doesn't have Managed Identity enabled."
                    Write-Host "Please enable Managed Identity on your Function App and run:" -ForegroundColor Yellow
                    Write-Host "  az keyvault set-policy --name $keyVaultName --object-id <principal-id> --secret-permissions get list" -ForegroundColor Yellow
                }

                # Store Key Vault URL in config
                $keyVaultUrl = "https://$keyVaultName.vault.azure.net/"
                Set-IniValue -Path $configFile -Section "Azure" -Key "KeyVaultUrl" -Value $keyVaultUrl

                Write-Host "`n✓ Key Vault setup complete" -ForegroundColor Green
                Write-Host "Key Vault URL: $keyVaultUrl" -ForegroundColor Cyan
            }
        } catch {
            Write-Warning "Key Vault setup failed: $($_.Exception.Message)"
            Write-Host "You can manually create the Key Vault and secret later." -ForegroundColor Yellow
        }
    } else {
        Write-Host "Skipping Key Vault setup. You can configure it later using Azure Portal or CLI." -ForegroundColor Yellow
    }

    Write-Host "`n✓ FinanceHub/QuoteHub SharePoint Provisioning Complete" -ForegroundColor Green
    Write-Host "Site URL: $siteUrl"
    Write-Host "Regional: LocaleId=$localeId, TimeZone='$timeZoneName', FirstDay=$firstDay, 24h=$use24hBool"
    Write-Host "Currency: $baseCurrencyCode (LCID $currencyLcid)"
    Write-Host "Config: $configFile"
    Write-Host "`nNew features:"
    Write-Host "  - Quotes List + Quote linkage to Invoices"
    Write-Host "  - CompanyLedger List for non-trading entries (salary, NI, PAYE, DLA, corporation tax, dividends)"
    Write-Host "  - Company Documents Library for key company documents"
    Write-Host "  - Customer/Supplier Codes (auto-generated by your app)"
    Write-Host "  - Customer Hourly Rate, Billing Address, VAT Registered flag"
    Write-Host "  - Invoice & Quote Discount fields (Percent, Amount, Note)"
    Write-Host "  - All currency fields configured with $baseCurrencyCode"

    Disconnect-PnPOnline | Out-Null
    exit 0
}
catch {
    Write-Host "`nERROR: $($_.Exception.Message)" -ForegroundColor Red
    Write-Host $_.ScriptStackTrace -ForegroundColor Red
